public class DataType {
    public static void main(String[]args){
        
        byte a= 12; // lenth -128 to 127 size - 1 bytes
        System.out.println(a);
       
        short q= 34; //    size - 2bytes
         System.out.println(q);

          char ch = 'c'; // a-z & A-Z & @ / *               size - 2bytes
         System.out.println(ch);
         
         int w= 6789; //   -2b to +2b             size - 4bytes
         System.out.println(w);
         
         long l= 3499; //                size - 8bytes
         System.out.println(l);
        
         boolean t = true;               // size - 1 bytes
         System.out.println(t);
        
         float f = 3.4f; //                size - 4bytes
         System.out.println(f);
         
         double d= 34.234567; //                size - 8bytes
         System.out.println(d);
    }
}
